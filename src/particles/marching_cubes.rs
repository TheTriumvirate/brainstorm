pub struct MarchingCubes {
    vertices: Buffer<f32>,
}

lazy_static! {
    static ref SHADER: OurShader = OurShader::new(
        str::from_utf8(OBJ_VERTEX_SHADER).expect("Failed to read vertex shader"),
        str::from_utf8(OBJ_FRAGMENT_SHADER).expect("Failed to read fragment shader"),
        &[
            ShaderAttribute {
                name: "a_position".to_string(),
                size: 3
            },
            ShaderAttribute {
                name: "a_normal".to_string(),
                size: 3
            },
        ]
    );
}

type Vector3 = (f32, f32, f32);

impl Drawable for MarchingCubes {
    fn get_shader(&self) -> Option<&OurShader> {
        Some(&SHADER)
    }

    fn draw_transformed(&self, view_matrix: &Matrix4<f32>) {
        let len = self.vertices.len() as i32 / 6;
        render_target::draw_vertex_array(DrawMode::TRIANGLES, 0, len, &self.vertices, self.render_states(), view_matrix)
    }
}

impl MarchingCubes {
    /// Sets the direction of the light illuminating the mesh.
    pub fn set_light_dir(&self, (x, y, z): Vector3) {
        let dist = (x * x + y * y + z * z).sqrt();
        SHADER.uniform3f("lightDir", x/dist, y/dist, z/dist);
    }

    /// Sets the transparency of the mesh.
    /// Argument should be 0.0 <= x <= 1.0.
    pub fn set_transparency(&self, transparency: f32) {
        SHADER.uniform1f("u_transparency", transparency);
    }

    pub fn marching_cubes(field: &FieldProvider) -> MarchingCubes {
        let mut vertices = Buffer::<f32>::new(BufferType::Array);
    
        const EPSILON: f32 = 0.0; // NOTE: 0.1 to reduce noise in data.
        const S : usize = 1; // step size

        let mut verts: [Vector3; 12] = [(0.0,0.0,0.0); 12];

        for x in (0..field.width).step_by(S) {
            for y in (0..field.height).step_by(S) {
                for z in (0..field.depth).step_by(S) {
                    let v1m = field.get_vec((x  , y  , z  )).3;
                    let v2m = field.get_vec((x+S, y  , z  )).3;
                    let v3m = field.get_vec((x+S, y  , z+S)).3;
                    let v4m = field.get_vec((x  , y  , z+S)).3;
                    let v5m = field.get_vec((x  , y+S, z  )).3;
                    let v6m = field.get_vec((x+S, y+S, z  )).3;
                    let v7m = field.get_vec((x+S, y+S, z+S)).3;
                    let v8m = field.get_vec((x  , y+S, z+S)).3;

                    let mut cidx : usize = 0;
                    if v1m > EPSILON {cidx |=   1;}
                    if v2m > EPSILON {cidx |=   2;}
                    if v3m > EPSILON {cidx |=   4;}
                    if v4m > EPSILON {cidx |=   8;}
                    if v5m > EPSILON {cidx |=  16;}
                    if v6m > EPSILON {cidx |=  32;}
                    if v7m > EPSILON {cidx |=  64;}
                    if v8m > EPSILON {cidx |= 128;}

                    let edges = EDGETABLE[cidx];
                    // This voxel is not on an edge
                    if edges == 0 {continue;}

                    let fx = x as f32 / field.width as f32;
                    let fy = y as f32 / field.height as f32;
                    let fz = z as f32 / field.depth as f32;

                    let dx = S as f32 / field.width as f32;
                    let dy = S as f32 / field.height as f32;
                    let dz = S as f32 / field.depth as f32;

                    MarchingCubes::push_edge(edges, 0,  &mut verts, EPSILON, (fx   , fy   , fz   ), (fx+dx, fy   , fz   ), v1m, v2m);
                    MarchingCubes::push_edge(edges, 1,  &mut verts, EPSILON, (fx+dx, fy   , fz   ), (fx+dx, fy   , fz+dz), v2m, v3m);
                    MarchingCubes::push_edge(edges, 2,  &mut verts, EPSILON, (fx+dx, fy   , fz+dz), (fx   , fy   , fz+dz), v3m, v4m);
                    MarchingCubes::push_edge(edges, 3,  &mut verts, EPSILON, (fx   , fy   , fz+dz), (fx   , fy   , fz   ), v4m, v1m);
                    MarchingCubes::push_edge(edges, 4,  &mut verts, EPSILON, (fx   , fy+dy, fz   ), (fx+dx, fy+dy, fz   ), v5m, v6m);
                    MarchingCubes::push_edge(edges, 5,  &mut verts, EPSILON, (fx+dx, fy+dy, fz   ), (fx+dx, fy+dy, fz+dz), v6m, v7m);
                    MarchingCubes::push_edge(edges, 6,  &mut verts, EPSILON, (fx+dx, fy+dy, fz+dz), (fx   , fy+dy, fz+dz), v7m, v8m);
                    MarchingCubes::push_edge(edges, 7,  &mut verts, EPSILON, (fx   , fy+dy, fz+dz), (fx   , fy+dy, fz   ), v8m, v5m);
                    MarchingCubes::push_edge(edges, 8,  &mut verts, EPSILON, (fx   , fy   , fz   ), (fx   , fy+dy, fz   ), v1m, v5m);
                    MarchingCubes::push_edge(edges, 9,  &mut verts, EPSILON, (fx+dx, fy   , fz   ), (fx+dx, fy+dy, fz   ), v2m, v6m);
                    MarchingCubes::push_edge(edges, 10, &mut verts, EPSILON, (fx+dx, fy   , fz+dz), (fx+dx, fy+dy, fz+dz), v3m, v7m);
                    MarchingCubes::push_edge(edges, 11, &mut verts, EPSILON, (fx   , fy   , fz+dz), (fx   , fy+dy, fz+dz), v4m, v8m);

                    let mut id = 0;

                    while TRIANGLETABLE[cidx][id] != -1 {

                        let v1 = verts[TRIANGLETABLE[cidx][id] as usize];
                        let v2 = verts[TRIANGLETABLE[cidx][id+1] as usize];
                        let v3 = verts[TRIANGLETABLE[cidx][id+2] as usize];
                        
                        let ab = (v2.0 - v1.0, v2.1 - v1.1, v2.2 - v1.2);
                        let cb = (v3.0 - v1.0, v3.1 - v1.1, v3.2 - v1.2);

                        let cross = (cb.1 * ab.2 - cb.2 * ab.1, cb.2 * ab.0 - cb.0 * ab.2, cb.0 * ab.1 - cb.1 * ab.0);
                        let dist = (cross.0 * cross.0 + cross.1 * cross.1 + cross.2 * cross.2).sqrt();
                        let norm = (cross.0 / dist, cross.1 / dist, cross.2 / dist);
                        
                        MarchingCubes::push_vert(&mut vertices, v1, norm);
                        MarchingCubes::push_vert(&mut vertices, v2, norm);
                        MarchingCubes::push_vert(&mut vertices, v3, norm);
                        id+= 3;
                    }

                }
            }
        }

        vertices.bind();
        let len = vertices.len();
        vertices.upload_data(0, len, true);

        MarchingCubes {
            vertices,
        }
    }

    fn push_edge(edges: u32, edge: usize, verts: &mut [Vector3], epsilon: f32, v1: Vector3, v2: Vector3, m1: f32, m2: f32) {
        if edges & (1<<edge) != 0 {
            let vert = MarchingCubes::interp(epsilon, v1, v2, m1, m2);
            
            verts[edge] = vert;
        }
    }

    fn push_vert(vertices: &mut Buffer<f32>, (x, y, z): Vector3, (nx, ny, nz): Vector3) {
        vertices.push(&[
            x - 0.5, y - 0.5, z - 0.5, // position
            nx, ny, nz // normals
        ])
    }

    fn interp(epsilon: f32, v1 : Vector3, v2 : Vector3, m1: f32, m2: f32) -> Vector3 {
        if (epsilon - m1).abs() < 0.00001 {
            return v1;
        }

        if (epsilon - m2).abs() < 0.00001 {
            return v2;
        }

        if (m1 - m2).abs() < 0.00001 {
            return v1;
        }

        let mu = (epsilon - m1) / (m2 - m1);
        let x = v1.0 + mu * (v2.0 - v1.0);
        let y = v1.1 + mu * (v2.1 - v1.1);
        let z = v1.2 + mu * (v2.2 - v1.2);
        (x, y, z)
    }

}

